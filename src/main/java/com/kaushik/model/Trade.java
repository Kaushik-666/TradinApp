package com.kaushik.model;

import java.time.LocalDate;

public class Trade {
	private Integer tradeId;
	private Integer version;
	private String counterPartyId;
	private String bookId;
	private LocalDate createdDate;
	private LocalDate maturityDate;
	private char expired;

	public Trade(Integer tradeId, Integer version, String counterPartyId, String bookId, LocalDate createdDate,
			LocalDate maturityDate, char expired) {
		super();
		this.tradeId = tradeId;
		this.version = version;
		this.counterPartyId = counterPartyId;
		this.bookId = bookId;
		this.createdDate = createdDate;
		this.maturityDate = maturityDate;
		this.expired = expired;
	}

	public Integer getTradeId() {
		return tradeId;
	}

	public void setTradeId(Integer tradeId) {
		this.tradeId = tradeId;
	}

	public Integer getVersion() {
		return version;
	}

	public void setVersion(Integer version) {
		this.version = version;
	}

	public String getCounterPartyId() {
		return counterPartyId;
	}

	public void setCounterPartyId(String counterPartyId) {
		this.counterPartyId = counterPartyId;
	}

	public String getBookId() {
		return bookId;
	}

	public void setBookId(String bookId) {
		this.bookId = bookId;
	}

	public LocalDate getMaturityDate() {
		return maturityDate;
	}

	public void setMaturityDate(LocalDate maturityDate) {
		this.maturityDate = maturityDate;
	}

	public LocalDate getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(LocalDate createdDate) {
		this.createdDate = createdDate;
	}

	public char getExpired() {
		return expired;
	}

	public void setExpired(char expired) {
		this.expired = expired;
	}

	@Override
	public String toString() {
		return "Trade [tradeId=" + tradeId + ", version=" + version + ", counterPartyId=" + counterPartyId + ", bookId="
				+ bookId + ", createdDate=" + createdDate + ", maturityDate=" + maturityDate + ", expired=" + expired
				+ "]";
	}

}
